#include <iostream>
using namespace std;

int main()
{

    for (int j = 0; j <= 18; j++) // 0-18
        cout << j << " ";

    cout << endl;

    for (int k = 25; k <= 45; k++) // 25-45
        cout << k << " ";

    cout << endl;

    for (int i = 40; i <= 80; i += 4) // 40-80 by 4's
        cout << i << " ";

    cout << endl;

    for (int i = 12; i >= -6; i -= 3) // 12--6 by -3's
        cout << i << " ";

    cout << endl;
}