#include <iostream>
using namespace std;

int main()
{

    for (int j = 70; j <= 80; j ++)
        cout << j << " ";

    cout << endl;

    for (int k = 100; k <= 300; k += 20)
        cout << k << " ";

    cout << endl;

    for (int i = 25; i >= -25; i -= 5)
        cout << i << " ";

    cout << endl;

    for (int i = 10000; i >= 10; i /= 5)
        cout << i << " ";

    cout << endl;
}